bb.I2CInit()
bb.setBrightness(100)
basic.forever(function () {
    serial.writeLine("Turn on the left light, motor forward, RGB light red")
    bb.setIndexColor(bb.ledRange(1, 4), bb.NeoPixelColors.Red)
    bb.controlMotor(bb.MyEnumMotor.AllMotor, bb.MyEnumDir.Forward, 100)
    bb.controlLED(bb.MyEnumLed.LeftLed, bb.MyEnumSwitch.Open)
    basic.pause(2000)
    serial.writeLine("Turn on the right side light, motor back, RGB light yellow")
    bb.setIndexColor(bb.ledRange(1, 4), bb.NeoPixelColors.Yellow)
    bb.controlMotor(bb.MyEnumMotor.AllMotor, bb.MyEnumDir.Backward, 100)
    bb.controlLED(bb.MyEnumLed.LeftLed, bb.MyEnumSwitch.Close)
    bb.controlLED(bb.MyEnumLed.RightLed, bb.MyEnumSwitch.Open)
    basic.pause(2000)
    serial.writeLine("Turn on the side lights, motor forward, RGB light green")
    bb.controlMotor(bb.MyEnumMotor.AllMotor, bb.MyEnumDir.Forward, 100)
    bb.controlLED(bb.MyEnumLed.AllLed, bb.MyEnumSwitch.Open)
    bb.setIndexColor(bb.ledRange(1, 4), bb.NeoPixelColors.Green)
    basic.pause(2000)
    serial.writeLine("Printing sensor data")
    serial.writeLine("SensorL1 status:" + bb.readLineSensorState(bb.MyEnumLineSensor.SensorL1))
    serial.writeLine("SensorL1 ADC:" + bb.readLineSensorData(bb.MyEnumLineSensor.SensorL1))
    serial.writeLine("ultrasonic sensor:" + bb.readUltrasonic(DigitalPin.P13, DigitalPin.P14))
})
